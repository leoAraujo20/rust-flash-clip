/*
Especificando tipos: 
No Rust, é possível especificar o tipo de uma variável usando a sintaxe: "let nome: tipo = valor". 
Isso é útil para garantir que a variável tenha o tipo correto e evitar erros de tipo em tempo de compilação.
*/

fn main (){
    let nome: &str = "João";
    let idade: i32 = 20;
    let nota: f64 = 9.5;
    let aprovado: bool = true;   
}